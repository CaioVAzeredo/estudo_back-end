public class FormaGeometrica {
    private String Cor;
    private String Nome;

    public FormaGeometrica(String cor, String nome) {
        Cor = cor;
        Nome = nome;
    }

    public String getNome() {
        return Nome;
    }

    public void setNome(String nome) {
        Nome = nome;
    }

    public String getCor() {
        return Cor;
    }

    public void setCor(String cor) {
        Cor = cor;
    }


}
